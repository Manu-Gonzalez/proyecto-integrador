/**
 * @swagger
 * /clients/{id}:
 *   get:
 *     summary: Obtener cliente por ID
 *     tags: [Clients]
 *     security:
 *       - bearerAuth: []
 *     parameters:
 *       - in: path
 *         name: id
 *         required: true
 *         schema:
 *           type: string
 *           format: uuid
 *         description: ID del cliente
 *     responses:
 *       200:
 *         description: Cliente encontrado
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Client'
 *       404:
 *         description: Cliente no encontrado
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: No autorizado
 */

export const clientsRoutes = () => {
  const router = Router();
  const clientController =
    container.resolve<ClientController>("clients-controller");

  router.get("/", authMiddlewareJWT, clientController.getAll);

  router.post("/add", authMiddlewareJWT, clientController.create);

  router.get("/:id", authMiddlewareJWT, clientController.getById)
  return router;
};
